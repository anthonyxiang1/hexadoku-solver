package finalprojGUI;

public class Tile {

	String letter;

	public Tile(String letter) {
		this.letter = letter;
	}

	public String getLetter() {
		return letter;
	}

	public void setLetter(String letter) {
		this.letter = letter;
	}

	public String toString() {
		return this.letter;
	}
}